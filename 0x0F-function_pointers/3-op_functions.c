#include "3-calc.h"

/**
 * op_add - add variables
 * @a: num1
 * @b: num2
 * Return: sum of num1 and num2
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts variables
 * @a: num1
 * @b: num2
 * Return: subtraction of num1 and num2
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies variables
 * @a: num1
 * @b: num2
 * Return: multiplication of num1 and num2
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides variables
 * @a: num1
 * @b: num2
 * Return: division of num1 and num2
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo variables
 * @a: num1
 * @b: num2
 * Return: modulo of num1 and num2
 */
int op_mod(int a, int b)
{
	return (a % b);
}
